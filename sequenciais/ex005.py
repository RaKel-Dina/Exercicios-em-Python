# faça um programa que leia um número inteiro e mostre na tela seu suscessor e seu antecessor.
num = int(input('Digite um número: '))
antecessor = num - 1
sucessor = num + 1
print(f'O antecessor de {num} é {antecessor} e o sucessor é {sucessor}.')
print(f'O antecessor de {num} é {num-1} e o sucessor é {num+1}')

